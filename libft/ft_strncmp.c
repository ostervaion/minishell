#include "libft.h"

int	ft_strncmp(const char *s1, char *s2, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n && *s1 != '\0' && *s2 != '\0')
	{
		if (*s1 != *s2)
			return ((size_t)(*s1 - *s2));
		s1++;
		s2++;
		i++;
	}
	if (i < n)
		return ((size_t)(*s1 - *s2));
	return (0);
}
